#operations
num_1 = 20
num_2 = 4

sum = num_1 + num_2
print(sum)

difference = num_1 - num_2
print(difference)

product = num_1 * num_2
print(product)

try:
    division = num_1 / num_2
    print(division)
except ZeroDivisionError: 
    print("division by zero")
    print("cannot be divided")
finally:
    print("done")




